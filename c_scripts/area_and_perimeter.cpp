#include <iostream>
#include <cmath>
using namespace std;


int main() {
    int length, breadth, area, perimeter;
    cout << "Enter the length: ";
    cin >> length;
    cout << "Enter the breadth: ";
    cin >> breadth;
    area = length * breadth;
    cout << "The area is: " << area << "\n";
    perimeter = area * area;
    cout << "The perimeter is: " << perimeter << "\n";
    return 0;

}

    

   
